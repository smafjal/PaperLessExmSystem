package sust.paperlessexm.tool;

/**
 *
 * @author Sm19
 */
public class PairIntStr {
    
    public int X;
    public String Y;

    public PairIntStr() {
    }

    public PairIntStr(int X, String Y) {
        this.X = X;
        this.Y = Y;
    }

    public int getX() {
        return X;
    }

    public String getY() {
        return Y;
    }

    public void setX(int X) {
        this.X = X;
    }

    public void setY(String Y) {
        this.Y = Y;
    }
    
}
